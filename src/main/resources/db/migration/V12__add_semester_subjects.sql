CREATE OR REPLACE FUNCTION validate_schedule_course_date()
RETURNS TRIGGER AS $$
DECLARE
    course_start DATE;
    course_end DATE;
BEGIN
    SELECT start_date, end_date
    INTO course_start, course_end
    FROM semester_subjects
    WHERE semester_id = NEW.semester_id
      AND subject_id = NEW.subject_id;

    IF course_start IS NULL THEN
        RETURN NEW;
    END IF;

    IF NEW.study_date < course_start OR NEW.study_date > course_end THEN
        RAISE EXCEPTION 'schedule date must stay within semester subject dates';
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER TR_schedule_validate_course_date
BEFORE INSERT OR UPDATE OF semester_id, subject_id, study_date
ON schedule
FOR EACH ROW
EXECUTE FUNCTION validate_schedule_course_date();

CREATE OR REPLACE FUNCTION validate_semester_update_keeps_subject_dates()
RETURNS TRIGGER AS $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM semester_subjects existing
        WHERE existing.semester_id = NEW.id
          AND (existing.start_date < NEW.start_date OR existing.end_date > NEW.end_date)
    ) THEN
        RAISE EXCEPTION 'semester dates must contain existing semester subject dates';
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER TR_semesters_validate_subject_dates
BEFORE UPDATE OF start_date, end_date
ON semesters
FOR EACH ROW
EXECUTE FUNCTION validate_semester_update_keeps_subject_dates();
