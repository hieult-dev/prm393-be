CREATE TABLE exam_schedules (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    user_id BIGINT NOT NULL,
    subject_id BIGINT NOT NULL,
    semester_id BIGINT NOT NULL,
    exam_type VARCHAR(50) NOT NULL DEFAULT 'FINAL',
    exam_date DATE NOT NULL,
    start_time TIME NOT NULL,
    end_time TIME NOT NULL,
    room VARCHAR(50) NULL,
    seat_number VARCHAR(20) NULL,
    proctor_name VARCHAR(100) NULL,
    note TEXT NULL,
    status VARCHAR(20) NOT NULL DEFAULT 'PUBLISHED',
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT PK_exam_schedules PRIMARY KEY (id),
    CONSTRAINT UQ_exam_schedules_user_subject_semester_type UNIQUE (user_id, subject_id, semester_id, exam_type),
    CONSTRAINT FK_exam_schedules_users FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT FK_exam_schedules_subjects FOREIGN KEY (subject_id) REFERENCES subjects(id),
    CONSTRAINT FK_exam_schedules_semesters FOREIGN KEY (semester_id) REFERENCES semesters(id),
    CONSTRAINT FK_exam_schedules_semester_subjects
        FOREIGN KEY (semester_id, subject_id) REFERENCES semester_subjects(semester_id, subject_id),
    CONSTRAINT CK_exam_schedules_time_order CHECK (end_time > start_time),
    CONSTRAINT CK_exam_schedules_status CHECK (status IN ('DRAFT', 'PUBLISHED', 'CANCELLED'))
);

CREATE INDEX IX_exam_schedules_user_semester
    ON exam_schedules(user_id, semester_id);

CREATE INDEX IX_exam_schedules_exam_date
    ON exam_schedules(exam_date, start_time);

